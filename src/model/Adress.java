package model;

import javaxt.utils.string;

public class Adress {
	private string street;
	private int sNum;
	private string city;
	private int postCode;
	public Adress(string street, int sNum, string city, int postCode) {
		super();
		this.street = street;
		this.sNum = sNum;
		this.city = city;
		this.postCode = postCode;
	}
	
	
}